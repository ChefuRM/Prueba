    public Integer insert(String query, Connection con) {
        try {
            Statement st = con.createStatement();
            st.executeUpdate(query);
            return 1;
        } catch (SQLException e) {
            e.printStackTrace();
            System.out.println("ERROR INSERT METHOD " + e.getErrorCode());
            return e.getErrorCode();
        }
    }

    public ArrayList<String[]> getDispositivos(Connection con) {
        ArrayList<String[]> queryResult = new ArrayList<>();
        ResultSet result = null;
        try ( Statement statement = con.createStatement();) {
            result = statement.executeQuery("SELECT * FROM Dispositivos WHERE IP_de_Dispositivo = " + Integer.parseInt(jIPDispositivo.getText()) + "");
            while (result.next()) {
                String[] obj = {result.getString(1), result.getString(2), result.getString(3), result.getString(4), result.getString(5)};
                queryResult.add(obj);
                System.out.println(result.getString(1) + result.getString(2) + result.getString(3) + result.getString(4) + result.getString(5));
            }
        } catch (SQLException e) {
            e.printStackTrace();
            System.out.println("ERROR SELECT METHOD " + e.getErrorCode());
        }
        return queryResult;
    }

